def country_centroid(code: str | None) -> tuple[float, float] | None:
    """Return the (lat, lon) centroid for an ISO alpha-2 country code, or ``None`` if unknown.

    Case-insensitive; a value that is not a known 2-letter code (or ``None``) yields ``None`` so the
    caller drops the point rather than plotting it wrongly.
    """
    if not code:
        return None
    return COUNTRY_CENTROIDS.get(code.strip().lower())


